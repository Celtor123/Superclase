
package exemplo_herdanza;

public class eliminar {
   String aa;
   int bb;
   float cc;

    public eliminar() {
    }

    public eliminar(String aa, int bb, float cc) {
        this.aa = aa;
        this.bb = bb;
        this.cc = cc;
    }

    public String getAa() {
        return aa;
    }

    public void setAa(String aa) {
        this.aa = aa;
    }

    public int getBb() {
        return bb;
    }

    public void setBb(int bb) {
        this.bb = bb;
    }

    public float getCc() {
        return cc;
    }

    public void setCc(float cc) {
        this.cc = cc;
    }

    

    

  

    
   
}
